// A cộng B
// input: Gồm  dòng chứa  số nguyên  A và B , cách bởi  dấu cách.
// output: Ghi ra tổng A + B.

#include "iostream"

int main() {
    int a, b;
    // Minh se viet theo cu phap cua C (khong phai C++);
    scanf("%d", &a);
    scanf("%d", &b);

    printf("%d", a + b);
    return 0;
}